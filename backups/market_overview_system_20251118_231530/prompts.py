def check_response_has_personality(response: str) -> bool:
    """
    –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, –µ—Å—Ç—å –ª–∏ –≤ –æ—Ç–≤–µ—Ç–µ —ç–ª–µ–º–µ–Ω—Ç—ã —Ö–∞—Ä–∞–∫—Ç–µ—Ä–∞ Syntra

    Args:
        response: –û—Ç–≤–µ—Ç –æ—Ç AI

    Returns:
        bool: True –µ—Å–ª–∏ –µ—Å—Ç—å —Ö–∞—Ä–∞–∫—Ç–µ—Ä, False –µ—Å–ª–∏ —Å–ª–∏—à–∫–æ–º –Ω–µ–π—Ç—Ä–∞–ª—å–Ω–æ
    """
    # –ò–Ω–¥–∏–∫–∞—Ç–æ—Ä—ã —Ö–∞—Ä–∞–∫—Ç–µ—Ä–∞
    personality_indicators = [
        # –ü—Ä—è–º—ã–µ —É–∫–∞–∑–∞—Ç–µ–ª–∏
        "–∫–ª–∞—Å—Å–∏–∫–∞",
        "–æ–±—ã—á–Ω–æ",
        "–∫–∞–∫ –≤—Å–µ–≥–¥–∞",
        "—Ç–∏–ø–∏—á–Ω–æ",
        # –í–æ–ø—Ä–æ—Å—ã (–∏—Ä–æ–Ω–∏—è)
        "?",
        # –õ–∏—á–Ω—ã–µ –æ–±—Ä–∞—â–µ–Ω–∏—è
        "—Ç—ã",
        "—Ç–µ–±–µ",
        "—Ç–≤–æ–π",
        # –ú–µ—Ç–∞—Ñ–æ—Ä—ã/–æ–±—Ä–∞–∑–Ω–æ—Å—Ç—å
        "—Ñ–ª–∏—Ä—Ç—É–µ—Ç",
        "–∏–≥—Ä–∞–µ—Ç",
        "—Ç–∞–Ω—Ü—É–µ—Ç",
        # AI self-awareness
        "—è",
        "–º–æ–π",
        # –°–ª–µ–Ω–≥/casual
        "—á—ë",
        "–Ω—É",
        "—Ç–∞–∫ —á—Ç–æ",
    ]

    response_lower = response.lower()

    # –°—á–∏—Ç–∞–µ–º –∏–Ω–¥–∏–∫–∞—Ç–æ—Ä—ã
    count = sum(1 for indicator in personality_indicators if indicator in response_lower)

    # –ï—Å–ª–∏ –º–µ–Ω—å—à–µ 2 –∏–Ω–¥–∏–∫–∞—Ç–æ—Ä–æ–≤ - –æ—Ç–≤–µ—Ç —Å–ª–∏—à–∫–æ–º –Ω–µ–π—Ç—Ä–∞–ª—å–Ω—ã–π
    return count >= 2


