def detect_sarcasm_level(user_message: str, context_tags: list = None) -> str:
    """
    –û–ø—Ä–µ–¥–µ–ª—è–µ—Ç —Ä–µ–∫–æ–º–µ–Ω–¥—É–µ–º—ã–π —É—Ä–æ–≤–µ–Ω—å —Å–∞—Ä–∫–∞–∑–º–∞ –Ω–∞ –æ—Å–Ω–æ–≤–µ –∫–æ–Ω—Ç–µ–∫—Å—Ç–∞

    Args:
        user_message: –°–æ–æ–±—â–µ–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        context_tags: –°–ø–∏—Å–æ–∫ —Ç–µ–≥–æ–≤ –∫–æ–Ω—Ç–µ–∫—Å—Ç–∞ (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω–æ)

    Returns:
        str: 'soft', 'medium', 'hard', –∏–ª–∏ 'disabled'
    """
    message_lower = user_message.lower()
    tags = context_tags or []

    # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ç—Ä–∏–≥–≥–µ—Ä—ã –¥–ª—è –æ—Ç–∫–ª—é—á–µ–Ω–∏—è —Å–∞—Ä–∫–∞–∑–º–∞
    for trigger in SARCASM_DISABLE_TRIGGERS:
        if trigger in tags or trigger.replace("_", " ") in message_lower:
            return "disabled"

    # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ç—Ä–∏–≥–≥–µ—Ä—ã –¥–ª—è —É—Å–∏–ª–µ–Ω–∏—è —Å–∞—Ä–∫–∞–∑–º–∞
    increase_count = 0
    for trigger in SARCASM_INCREASE_TRIGGERS:
        if trigger in tags or trigger.replace("_", " ") in message_lower:
            increase_count += 1

    # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ç—Ä–∏–≥–≥–µ—Ä—ã –¥–ª—è –æ—Å–ª–∞–±–ª–µ–Ω–∏—è —Å–∞—Ä–∫–∞–∑–º–∞
    decrease_count = 0
    for trigger in SARCASM_DECREASE_TRIGGERS:
        if trigger in tags or trigger in message_lower:
            decrease_count += 1

    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º —É—Ä–æ–≤–µ–Ω—å
    if increase_count >= 2:
        return "hard"
    elif increase_count > 0 and decrease_count == 0:
        return "medium"
    elif decrease_count > 0:
        return "soft"
    else:
        return "medium"  # –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é


