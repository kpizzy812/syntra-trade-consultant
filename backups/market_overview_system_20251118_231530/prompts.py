    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ NFA –≤ –∫–æ–Ω—Ü–µ
    has_nfa = "NFA" in response[-100:] or "DYOR" in response[-100:]

